import pulp as pl

# Створюємо лінійну модель (задачу) з максимізацією
model = pl.LpProblem("Оптимізація_виробництва", pl.LpMaximize)

# Визначаємо змінні: 
# x – кількість одиниць "Лимонаду"
# y – кількість одиниць "Фруктового соку"
x = pl.LpVariable("Лимонад", lowBound=0, cat="Integer")
y = pl.LpVariable("Фруктовий_сік", lowBound=0, cat="Integer")

# Функція мети: максимізувати загальну кількість вироблених продуктів
model += x + y, "Загальна_виробництво"

# Обмеження ресурсів:

# 1. Обмеження за "Водою": 
# Для "Лимонаду" потрібно 2 од. води, для "Фруктового соку" – 1 од.
model += 2 * x + y <= 100, "Обмеження_води"

# 2. Обмеження за "Цукром": 
# Для "Лимонаду" потрібно 1 од. цукру
model += x <= 50, "Обмеження_цукру"

# 3. Обмеження за "Лимонним соком": 
# Для "Лимонаду" потрібно 1 од. лимонного соку
model += x <= 30, "Обмеження_лимонного_соку"

# 4. Обмеження за "Фруктовим пюре": 
# Для "Фруктового соку" потрібно 2 од. фруктового пюре
model += 2 * y <= 40, "Обмеження_фруктового_пюре"

# Розв'язання задачі
model.solve()

# Виведення результатів
print("Статус розв'язку:", pl.LpStatus[model.status])
print("Кількість виробленого 'Лимонаду':", x.varValue)
print("Кількість виробленого 'Фруктового соку':", y.varValue)
print("Загальна кількість вироблених продуктів:", pl.value(model.objective))
